        <h1>What to do for Post covid fatigue?</h1>
        <section id="p1" data-aos="zoom-in" data-aos-offset="300"data-aos-easing="ease-in-sine">
        <p>

          Consume energy-boosting foods like banana, apples, oranges or sweet lime juice to deal with post-COVID fatigue. Add sweet potato in the salad or as a part of your meal. Take warm water with organic honey and lime.<br>
          Drink plenty of fluids, like warm water with tulsi leaves to manage COVID symptoms, like cough and itchy throat. Avoid sugary drinks, alcohol, coffee as it leads to dehydration. Inhale steam with the tongue out at least 2-3 times a day.Fruits and vegetables do not transmit the virus directly, but they should be thoroughly washed before cooking or consumption. They should be ideally washed in warm water or you can add soda-bicarb to the water. Soak the vegetables and fruits in this water for some time before using it.Maintain good fluid balance as ‘Hydration is the key’ to avoid urinary infections. Incorporate good amounts of Vitamin C in the diet, up to 1-2g/day.
