        <h1><b>03/8Diaphragmatic breathing</b></h1>
         <section id="img1" data-aos="fade-right" data-aos-offset="300"data-aos-easing="ease-in-sine">
        <img src="img/11.jpg"alt="" class="img-responsive floating-image left">
      </section>
        <p>
          Also known as belly breathing, doing this exercise helps improve the function of the diaphragm and get more air to the base of the lungs, facilitating easy breathing.<br>




To do this, sit relaxed or lie down comfortably. Resting well, place the tip of the tongue behind your top front teeth. Keep your back straight and eyes closed. Attempt breathing normally and then place one hand on your chest, and one on your abdomen.<br>



Breathe deeply through your nose, expanding your ribs and feeling your stomach expand outwards. Exhale, stretching stomach inwards. Breathe slowly and deep in a similar manner for upto 10 times.
        </p><br>
        <h3><b>04/8 Pursed lip breathing</b></h3>
        <section id="img1" data-aos="fade-down" data-aos-offset="300"data-aos-easing="ease-in-sine">
        <img src="img/12.jpg"alt="" class="img-responsive floating-image left"></section>
        <p>
         Pursed lip breathing exercises are one of the best techniques to increase oxygen intake and focus on keeping your airways open.<br>


To try this technique, sit in a relaxed position. Now, breathe in slowly through the nose, for several counts. The mouth should remain closed. Before you exhale, purse your lips and then slowly breathe out all the air in your lungs.<br>



The aim of pursed breathing is to try and exhale longer breath counts than you inhale. Perform this exercise several times for maximum benefits.
        </p><br>
        <h3><b>05/8 Yawn to a smile</b></h3>
        <section id="img1" data-aos="fade-up" data-aos-offset="300"data-aos-easing="ease-in-sine">
        <img src="img/13.jpg"alt="" class="img-responsive floating-image left"></section>
        <p>
          This breathing exercise facilitates opening up of the muscles in the chest, which then allow the diaphragm to fully expand and regulate oxygen flow.<br>


To perform this exercise, start by sitting upright with your back straight. Now, stretch your arms up till the shoulders. Now, open your mouth wide as if you are trying to yawn. Doing so, bring your arms back to rest on your thighs and turn that yawn into a smile.
        </p><br>
